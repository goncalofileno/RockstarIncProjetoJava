package Objetos;

import java.time.LocalDate;

public class Preco {
    //////////////////////////////////////////ATRIBUTOS/////////////////////////////////////////////////////////////////
    private double valor;
    private LocalDate dataPreco;
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////CONSTRUTORES//////////////////////////////////////////////////////////////
    public Preco(double valor, LocalDate dataPreco) {
        this.valor = valor;
        this.dataPreco = dataPreco;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////GETTERS/////////////////////////////////////////////////////////////////
    public double getValor() {
        return valor;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
